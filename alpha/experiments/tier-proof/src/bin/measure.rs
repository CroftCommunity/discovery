//! `measure` — emit the P9 scale numbers (the deliverable) as Markdown.
//!
//! Run `cargo run --release --bin measure > MEASUREMENTS.md`. No boundary number
//! is chosen — the numbers inform; the owner decides (§4 non-goal).

use std::collections::HashSet;

use tier_proof::scale;

fn main() {
    println!("# RUN-17 P9 — Measured scale numbers\n");
    println!("_Generated by `cargo run --release --bin measure`. Single-core, this");
    println!("environment. No boundary number is chosen here — the numbers inform, the");
    println!("owner decides (§4 non-goal)._\n");

    // (a) roster size + inclusion proof.
    println!("## (a) Backplane verification throughput\n");
    let roster = scale::synthetic_roster(100_000);
    let bytes = scale::roster_bytes(&roster);
    println!(
        "- **Synthetic roster @ 100k:** {} entries, {:.2} MB of bare DIDs ({} B). With",
        roster.len(),
        bytes as f64 / 1_000_000.0,
        bytes
    );
    println!("  per-member metadata (interval set + co-sign head reference, ~40 B each) a full",);
    println!("  roster approaches ~10 MB — the figure the light-client proof below sidesteps.");

    let root = scale::merkle_root(&roster);
    let proof = scale::inclusion_proof(&roster, 42_000);
    let ok = scale::verify_inclusion(root, &roster[42_000], 42_000, &proof);
    println!(
        "- **Light-client inclusion proof @ 100k:** {} levels, {} B (verifies: {}).",
        proof.len(),
        scale::proof_size_bytes(&proof),
        ok
    );
    println!(
        "  A light client checks membership against the co-signed roster head in {} hashes,",
        proof.len()
    );
    println!(
        "  never downloading the {:.1} MB roster.\n",
        bytes as f64 / 1_000_000.0
    );

    let roster_set: HashSet<String> = roster.iter().take(1_000).cloned().collect();
    let t = scale::measure_verify_throughput(50_000, &roster_set);
    println!(
        "- **Verification throughput:** {:.0} envelope verifications/sec single-core",
        t.per_second
    );
    println!(
        "  ({} verified in {:.3} s; each = ed25519 signature verify + roster-at-position lookup).\n",
        t.verified, t.seconds
    );
    println!(
        "  _Supports:_ a single core sustains ~{:.0}k verifications/sec, so backplane",
        t.per_second / 1000.0
    );
    println!("  verification is not the scaling wall — roster distribution and storage are.\n");

    // (b) churn / epoch ceiling.
    println!("## (b) The sealed ceiling — membership churn\n");
    println!("_Local epoch model (`SPEC-DELTA[run17-churn-model]`); the croft-group group-seal");
    println!("MLS harness builds cleanly and would back this at loopback grade. Epoch-roll cost");
    println!("is a measured O(N) computation; the contradiction rate is a modelled no-arbiter");
    println!("collision rate._\n");
    println!("| N | epoch-roll (µs) | concurrent commits | contradiction rate |");
    println!("|---|---|---|---|");
    for n in [8usize, 64, 256, 1024, 4096] {
        let c = scale::simulate_churn(n, 100);
        println!(
            "| {} | {:.2} | {} | {:.3} |",
            c.n,
            c.epoch_roll_nanos as f64 / 1000.0,
            c.concurrent_commits,
            c.contradiction_rate
        );
    }
    println!();
    println!("_Supports:_ epoch-roll cost grows ~linearly in N (an MLS rekey touches every");
    println!("member), so the sealed steward group has a soft ceiling where roll cost and");
    println!("concurrent-commit contradictions dominate — the boundary the owner sets, informed");
    println!("by this curve, not chosen here.");
}
